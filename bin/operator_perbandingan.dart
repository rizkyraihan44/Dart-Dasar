void main() {
  print(10 == 10); // output true
  print(10 != 10); // output false
  print(10 > 10); // output false
  print(10 < 10); // output false
  print(10 >= 10); // output true
  print(10 <= 10); // output true

  print('rizky' == 'rizky'); // output true
  print('rizky' != 'rizky'); // output false
}
