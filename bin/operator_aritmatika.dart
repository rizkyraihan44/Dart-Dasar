void main() {
  // Operator Aritmatika
  var first = 10;
  var second = 3;

  print(first + second); // output 13
  print(first - second); // output 7
  print(first * second); // output 30
  print(first / second); // pembagian hasil double   // output 3.3333333333
  print(first ~/ second); // pembagian hasil int     // output 3
  print(first % second); // modulo/ sisa bagi        // output 1
}
